public class inString {
    private String str;
    private char letter;

    public inString() {
    }

    public inString(String str, char letter) {
        this.str = str;
        this.letter = letter;
    }

    public String getStr() {
        return str;
    }

    public void setStr(String str) {
        this.str = str;
    }

    public char getLetter() {
        return letter;
    }

    public void setLetter(char letter) {
        this.letter = letter;
    }
    
    
    
    
}
